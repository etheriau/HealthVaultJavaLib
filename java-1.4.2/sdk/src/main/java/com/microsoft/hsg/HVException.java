package com.microsoft.hsg;

/**
 * The Class HVException is the base class for all HV Exceptions.
 * 
 */
public class HVException extends RuntimeException
{
	
	/**
	 * Instantiates a new hV exception.
	 */
	public HVException()
	{
		super();
	}
	
	/**
	 * Instantiates a new hV exception.
	 * 
	 * @param e the Exception 
	 */
	public HVException(Exception e)
	{
		super(e);
	}
	
	/**
	 * Instantiates a new hV exception.
	 * 
	 * @param msg the msg
	 */
	public HVException(String msg)
	{
		super(msg);
	}
	
	/**
	 * Instantiates a new hV exception.
	 * 
	 * @param msg the msg
	 * @param e the Exception
	 */
	public HVException(String msg, Exception e)
	{
		super(msg,e);
	}
}


